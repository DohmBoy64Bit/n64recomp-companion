def ensure_minimal_fixtures(base: str | Path | None = None) -> Path:
    fixture_dir = Path(base) if base is not None else Path(__file__).resolve().parent / "fixtures"
    fixture_dir.mkdir(parents=True, exist_ok=True)
    for filename, encoded in _FIXTURE_B64.items():
        path = fixture_dir / filename
        expected = base64.b64decode(encoded)
        if not path.exists() or path.read_bytes() != expected:
            path.write_bytes(expected)
    return fixture_dir
